// ===================== Tema (icon-only) =====================
(function themeInit(){
  const root = document.documentElement;
  const saved = localStorage.getItem('theme') || 'dark';
  root.setAttribute('data-bs-theme', saved);
  const btn = document.getElementById('themeBtn');
  const setLabel = () => {
    const lang = localStorage.getItem('lang') || 'es';
    const dict = I18N[lang];
    if (btn && btn.querySelector('span')) {
      btn.querySelector('span').textContent = dict.toggleTheme; // vacío a propósito
      btn.firstElementChild.className = root.getAttribute('data-bs-theme')==='dark' ? 'bi bi-brightness-high me-1' : 'bi bi-moon-stars me-1';
    }
  };
  setLabel();
  btn?.addEventListener('click', () => {
    const next = root.getAttribute('data-bs-theme') === 'dark' ? 'light' : 'dark';
    root.setAttribute('data-bs-theme', next);
    localStorage.setItem('theme', next);
    setLabel();
  });
})();


// ===================== i18n =====================
(function i18n(){
  const setPlaceholders = (dict) => {
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el=>{
      const key = el.getAttribute('data-i18n-placeholder');
      if (dict[key]) el.setAttribute('placeholder', dict[key]);
    });
    document.querySelectorAll('[data-i18n-aria]').forEach(el=>{
      const key = el.getAttribute('data-i18n-aria');
      if (dict[key]) el.setAttribute('aria-label', dict[key]);
    });
  };

  const applyLang = (lang) => {
    const dict = I18N[lang] || I18N.es;

    // Texto plano
    document.querySelectorAll('[data-i18n]').forEach(el=>{
      const key = el.getAttribute('data-i18n');
      if(dict[key]) el.textContent = dict[key];
    });

    // NUEVO: HTML (para casos con enlaces dentro del contenido)
    document.querySelectorAll('[data-i18n-html]').forEach(el=>{
      const key = el.getAttribute('data-i18n-html');
      if(dict[key]) el.innerHTML = dict[key];
    });

    setPlaceholders(dict);

    const lbl = document.getElementById('langLabel');
    if (lbl) lbl.textContent = lang.toUpperCase();
    document.documentElement.lang = lang;
    localStorage.setItem('lang', lang);

    // (Si usas contador de skills, aquí puedes refrescarlo como ya lo hacías)
  };

  const saved = localStorage.getItem('lang') || 'es';
  applyLang(saved);
  document.querySelectorAll('[data-lang]')?.forEach(btn=>{
    btn.addEventListener('click', ()=> applyLang(btn.dataset.lang));
  });
})();


// ===================== Botón Scroll Up =====================
(function backToTop(){
  const btn = document.getElementById('backToTop');
  if(!btn) return;

  const root = document.documentElement;

  function scrollPercent(){
    const st = root.scrollTop || document.body.scrollTop;
    const sh = root.scrollHeight || document.body.scrollHeight;
    const ch = root.clientHeight;
    const max = Math.max(1, sh - ch);
    return st / max; // 0.0 - 1.0
  }

  // Lerp helpers para color
  function lerp(a,b,t){ return a + (b - a) * t; }
  function lerpColor(c1, c2, t){
    const r = Math.round(lerp(c1[0], c2[0], t));
    const g = Math.round(lerp(c1[1], c2[1], t));
    const b = Math.round(lerp(c1[2], c2[2], t));
    return `rgb(${r}, ${g}, ${b})`;
  }
  function setBtnColor(p){
    const start = 0.0, end = 1.00;
    const t = Math.min(1, Math.max(0, (p - start)/(end - start)));
    const green = [25,135,84];  // #198754
    const red   = [220,53,69];  // #dc3545
    const color = lerpColor(green, red, t);
    btn.style.setProperty('--to-top-bg', color);
  }

  function onScroll(){
    const p = scrollPercent();
    if (p >= 0.05) {
      btn.classList.add('is-visible');
    } else {
      btn.classList.remove('is-visible');
    }
    setBtnColor(p);
  }

  // Mostrar/ocultar y colorear al cargar + eventos
  onScroll();
  window.addEventListener('scroll', onScroll, { passive: true });
  window.addEventListener('resize', onScroll);

  // Subir al inicio (respeta prefers-reduced-motion)
  const prefersReduced = window.matchMedia('(prefers-reduced-motion: reduce)').matches;
  btn.addEventListener('click', () => {
    if (prefersReduced) {
      window.scrollTo(0, 0);
    } else {
      window.scrollTo({ top: 0, behavior: 'smooth' });
    }
  });
})();
